#define FBK_UPARROW         0xC8   // UP -> SDLK_UP (0x111)
#define FBK_DOWNARROW       0xD0   // DOWN -> SDLK_DOWN (0x112)
#define FBK_LEFTARROW       0xCB   // LEFT -> SDLK_LEFT (0x114)
#define FBK_RIGHTARROW      0xCD   // RIGHT -> SDLK_RIGHT (0x113)
#define FBK_ESCAPE          0x01   // MENU -> SDLK_ESCAPE (0x1B)
#define FBK_SPACE           0x39   // A -> SDLK_SPACE (0x20)
#define FBK_LCONTROL        0x1D   // B -> SDLK_LCTRL (0x132)
#define FBK_LSHIFT          0x2A   // X -> SDLK_LSHIFT (0x130)
#define FBK_LALT            0x38   // Y -> SDLK_LALT (0x134)
#define FBK_E               0x12   // L1 -> SDLK_e (0x65)
#define FBK_T               0x14   // R1 -> SDLK_t (0x74)
#define FBK_TAB             0x0F   // L2 -> SDLK_TAB (0x9)
#define FBK_BACK            0x0E   // R2 -> SDLK_BACKSPACE (0x8)
#define FBK_RCONTROL        0x9D   // SELECT -> SDLK_RCTRL (0x131)
#define FBK_RETURN          0x1C   // START -> SDLK_RETURN (0xD)
#define FBK_RWIN            0xDC   // VOL+ -> SDLK_RSUPER (0x138)
#define FBK_LWIN            0xDB   // VOL- -> SDLK_LSUPER (0x137)
#define FBK_POWER           0xDE   // POWER -> SDLK_POWER (0x140)
