// inp_sdl_keys.h

// Definición de mapeo de teclas de SDL a FB Alpha
extern int SDLtoFBK[512]; // Declaración del array en el archivo de cabecera

// Definición de las teclas usadas por Miyoo Mini en términos de códigos de FB Alpha
#define FBK_UPARROW         0xC8   // UP Arrow
#define FBK_DOWNARROW       0xD0   // DOWN Arrow
#define FBK_LEFTARROW       0xCB   // LEFT Arrow
#define FBK_RIGHTARROW      0xCD   // RIGHT Arrow
#define FBK_ESCAPE          0x01   // MENU (ESCAPE)
#define FBK_SPACE           0x39   // A (SPACE)
#define FBK_LCONTROL        0x1D   // B (Left Control)
#define FBK_LSHIFT          0x2A   // X (Left Shift)
#define FBK_LALT            0x38   // Y (Left Alt)
#define FBK_E               0x12   // L1 (E)
#define FBK_T               0x14   // R1 (T)
#define FBK_TAB             0x0F   // L2 (TAB)
#define FBK_BACK            0x0E   // R2 (Backspace)
#define FBK_RCONTROL        0x9D   // SELECT (Right Control)
#define FBK_RETURN          0x1C   // START (Enter)
#define FBK_RWIN            0xDC   // VOL+ (Right Windows Key)
#define FBK_LWIN            0xDB   // VOL- (Left Windows Key)
#define FBK_POWER           0xDE   // POWER
